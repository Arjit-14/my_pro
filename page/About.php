                            CHRIST (Deemed to be University) was born out of the educational vision of St Kuriakose Elias Chavara, an educationalist and social reformer of the nineteenth century in South India. He founded the first Catholic indigenous congregation, Carmelites of Mary Immaculate
                            (CMI), in 1831, which administers CHRIST (Deemed to be University). CHRIST was established on 15 July 1969 as an affiliated college, with the Vision of 'Excellence and Service'. It became a preferred educational institution in Bangalore, Delhi-NCR and Lavasa within
                            the short period of inception. One of the first institutions in India to be accredited in 1998 by the NAAC, and subsequently in 2004 and 2016, CHRIST (Deemed to be University) has the top grade ‘A’ in the 4-point scale.
