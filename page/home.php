            <h3>About Conference</h3>
            <div class="description-one">
                <p style="text-align: justify;">
                    The aim of the International Conference on Smart Cyber Physical Systems (ICSCPS-2024) is to bring together the academicians, researchers, and industrial professionals to discuss and exchange their knowledge, innovative ideas, latest issues, advanced technologies, and advancements in the area of Sustainable Development Goals, Cyber physical systems, and related applications that will be presented in the conference. Cyber-physical systems (CPS) are societal-scale engineered systems that are built from and depend upon the seamless, correct and safe integration of physical processes, computation components, and, in most cases the human supervisors. As a multi-disciplinary discipline, CPS engineering requires theoretical analysis, advanced heuristics, artificial intelligence, security methodologies and big data to work seamlessly together. The deployment of CPS is profoundly transforming how we interact with the physical world, just as the world wide web transformed how we interact with one another. The impact of CPS science is visible across a number of challenging domains, including autonomous systems, agriculture, aeronautics, building design, civil infrastructure, energy, environmental quality, healthcare and personalized medicine, manufacturing, and transportation. Further integration and innovations in CPS will continue to have an enormous societal and economic impact.<br><br>

                    The International Conference on Smart Cyber Physical Systems (ICSCPS-2024) will act as an International platform to share and demonstrate novel approaches, development, technology improvement and research among interested researchers, students, developers, and practitioners. The conference invites and encourages the submissions in terms of concepts, surveys, state-of-art original and unpublished research, running implementation, applications, industrial case studies and in-progress research that can be categorized among regular and poster papers. The conference will be having many plenary and invited keynote talks by the international reputed speakers covering the latest topic of a wide range of interested researchers.<br><br>
